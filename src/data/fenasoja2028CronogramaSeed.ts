type SeedInput = Omit<
  CronogramaEventSeed,
  'isOfficialSeed' | 'status' | 'priority' | 'hasExactDate' | 'startDate' | 'endDate'
> & {
  startDate?: string | null;
  endDate?: string | null;
  status?: CronogramaStatus;
  priority?: CronogramaPriority;
  hasExactDate?: boolean;
};

function normalizeEvent(input: SeedInput): CronogramaEventSeed {
  const commission = input.commissionSlug ? commissionBySlug.get(input.commissionSlug) : null;
  const linkedCommissions = input.linkedCommissions?.length
    ? input.linkedCommissions
    : commission
      ? [commission]
      : [];

  return {
    ...input,
    startDate: input.startDate ?? null,
    endDate: input.endDate ?? null,
    status: input.status ?? (input.hasExactDate === false ? 'aguardando_definicao' : 'planejado'),
    priority: input.priority ?? (input.eventType === 'feriado' ? 'baixa' : 'media'),
    hasExactDate: input.hasExactDate ?? Boolean(input.startDate),
    commissionName: input.commissionName ?? commission?.name ?? null,
    linkedCommissions,
    subevents: input.subevents ?? [],
    isOfficialSeed: true,
  };
}

function dated(
  baseKey: string,
  dates: string[],
  input: Omit<SeedInput, 'sourceKey' | 'startDate' | 'endDate' | 'hasExactDate'>,
): CronogramaEventSeed[] {
  return dates.map((date) =>
    normalizeEvent({
      ...input,
      sourceKey: `${baseKey}-${date}`,
      startDate: date,
      endDate: null,
      hasExactDate: true,
    }),
  );
}

function period(
  sourceKey: string,
  startDate: string,
  endDate: string,
  input: Omit<SeedInput, 'sourceKey' | 'startDate' | 'endDate' | 'hasExactDate'>,
): CronogramaEventSeed {
  return normalizeEvent({
    ...input,
    sourceKey,
    startDate,
    endDate,
    hasExactDate: true,
  });
}

function undated(
  sourceKey: string,
  input: Omit<SeedInput, 'sourceKey' | 'startDate' | 'endDate' | 'hasExactDate' | 'status'>,
): CronogramaEventSeed {
  return normalizeEvent({
    ...input,
    sourceKey,
    startDate: null,
    endDate: null,
    hasExactDate: false,
    status: 'aguardando_definicao',
    eventType: input.eventType === 'sem_data' ? 'sem_data' : input.eventType,
  });
}

